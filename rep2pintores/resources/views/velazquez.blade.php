            <h3>Pintor de pintores» (según Manet), maestro indiscutible del arte universal, genio del barroco sevillano, «el más grande pintor que jamás ha existido» (dice Dalí) ….
                Diego Rodríguez de Silva y Velázquez es quizás el mejor pintor español de la historia. Su obra fue ampliamente estudiada por maestros de épocas posteriores e influiría en artistas tan dispares como los anteriormente citados Dalí o Manet.
                Atraído por la nueva iluminación tenebrista de Caravaggio fue uno de los introductores del estilo en España y por su evidente talento (y quizás un poco de ambición) se convirtió en pintor de la corte de Felipe IV en la que fue la época más esplendorosa de la cultura española (el siglo de oro).
                Sin embargo, no fue hasta el siglo XIX que alcanzó el status de leyenda, cuando los impresionistas lo convirtieron en referente. Desde entonces Velázquez es aclamado como «el mejor pintor de la historia».
                Desde muy joven Diego Velázquez dio muestras de su talento y pudo ingresar en el taller de Francisco Pacheco. Entre maestro y discípulo se creó una firme amistad basada en la gratitud de Velázquez y el orgullo de Pacheco. Incluso le ofreció la mano de su hija Juana su aventajado alumno.
                Muy pronto se le hace pequeña Sevilla e intenta una posición en la corte de Madrid, donde sabe que el monarca Felipe IV, a pesar de sus pocas luces como estadista, es un gran aficionado a las artes (y a las mujeres). No lo consigue y regresa a Sevilla donde empieza su primera época con un estilo manierista muy veneciano, pero adoptando los claroscuros de Caravaggio. Es el realismo barroco, que arrasa en la época, y que el pintor maneja como nadie lo había hecho hasta ahora.
                En su segundo viaje a Madrid, ahora si recibe Velázquez la atención del conde duque de Olivares (el que mandaba en España, en realidad), quien lo recomienda para el próximo retrato del rey. Felipe IV quedó tan complacido por esta obra que, nada más verla, lo nombró pintor de la corte, trasladándose el artista al Palacio Real y pudiendo viajar asiduamente a Italia (con el natural impacto que eso supone).
                Aunque más que pintor de corte, el crecido Velázquez quería ser tratado como «Asesor cultural». El caso es que disfrutó desde entonces un sueldo fijo y acceso a toda la cultura de la época. De sevillano de a pie a figura importante del palacio en pocos años.
                Es por ello que muchos creen que además de su innegable talento, Velázquez era bastante trepa y muy pelota con el rey (que era sólo cuatro años menor que él). Muchos artistas lo pasaron muy mal en la época, pero él supo ascender y en alguna ocasión mediante golpes bajos para eliminar toda competencia.
                Fue por lo que se creó no pocos enemigos, que entre otros rumores, esparcieron el de que Velazquez era homosexual o tenía «sangre poco pura» (judío, se entiende…).
                Al final, 2 años antes de morir, cumplió su sueño de ser noble. Después de todo, pese a ser quizás el mejor del mundo en la época, repudiaba el trabajo manual. En esa época fue bastante vago y pintó pocos cuadros al año, dejando muchísimos inacabados,
                A divinos brochazos, Velázquez pinta reyes, pero también bufones y «sabandijas de palacio». Pinta Venus desnudas, Bacos borrachos y también cuadros históricos, o familias reales en composiciones modernísimas, con estilos asombrosos.

